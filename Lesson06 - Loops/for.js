/*      *** Lesson06 Part02 - Loop For ***

    For is used when we already know how many times we want to repeat our code

    Structure: 
    for (iterator; condition; increment) {

        executes the scope

    } 

    // Similar to the while, but the diference is the while is used when we do not know how many times we need to repeat the code. However, for loop we already know.

*/

console.log("Print 10 Hello World!");
for (let i = 0; i < 10; i++) {

    // It will execute 10 times 
    console.log("Hello, World!");
    
}
